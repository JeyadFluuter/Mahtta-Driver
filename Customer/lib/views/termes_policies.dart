            unselectedLabelColor: Colors.grey,
            tabs: const [
              Tab(text: 'سياسة الخصوصية'),
              Tab(text: 'الأحكام والشروط'),
            ],
          ),
        ),
        body: Column(
          children: [
            const SizedBox(height: AppDimensions.paddingSmall),
            _buildUserTypeToggle(),
            Expanded(
              child: TabBarView(
                controller: _mainTabController,
                children: [
                  _buildContentList(_getPrivacySections(_userTypeIndex == 0)),
                  _buildContentList(_getTermsSections(_userTypeIndex == 0)),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildUserTypeToggle() {
    return Container(
      margin: const EdgeInsets.symmetric(horizontal: AppDimensions.paddingMedium),
      padding: const EdgeInsets.all(4),
      decoration: BoxDecoration(
        color: Colors.grey.shade100,
        borderRadius: BorderRadius.circular(12),
      ),
      child: Row(
        children: [
          Expanded(
            child: GestureDetector(
              onTap: () => setState(() => _userTypeIndex = 0),
              child: Container(
                padding: const EdgeInsets.symmetric(vertical: 8),
                decoration: BoxDecoration(
                  color: _userTypeIndex == 0 ? Colors.white : Colors.transparent,
                  borderRadius: BorderRadius.circular(8),
                  boxShadow: _userTypeIndex == 0
                      ? [BoxShadow(color: Colors.black12, blurRadius: 4)]
                      : [],
                ),
                alignment: Alignment.center,
                child: Text(
                  'للمستخدمين',
                  style: TextStyle(
                    fontWeight: _userTypeIndex == 0 ? FontWeight.bold : FontWeight.normal,
                    color: _userTypeIndex == 0 ? AppThemes.primaryNavy : Colors.grey,
                  ),
                ),
              ),
            ),
          ),
          Expanded(
            child: GestureDetector(
              onTap: () => setState(() => _userTypeIndex = 1),
              child: Container(
                padding: const EdgeInsets.symmetric(vertical: 8),
                decoration: BoxDecoration(
                  color: _userTypeIndex == 1 ? Colors.white : Colors.transparent,
                  borderRadius: BorderRadius.circular(8),
                  boxShadow: _userTypeIndex == 1
                      ? [BoxShadow(color: Colors.black12, blurRadius: 4)]
                      : [],
                ),
                alignment: Alignment.center,
                child: Text(
                  'للسائقين',
                  style: TextStyle(
                    fontWeight: _userTypeIndex == 1 ? FontWeight.bold : FontWeight.normal,
                    color: _userTypeIndex == 1 ? AppThemes.primaryNavy : Colors.grey,
                  ),
                ),
              ),
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildContentList(List<Map<String, String>> sections) {
    return ListView.builder(
      padding: const EdgeInsets.all(AppDimensions.paddingMedium),
      itemCount: sections.length,
      itemBuilder: (context, index) {
        return _buildSection(sections[index]['title']!, sections[index]['content']!);
      },
    );
  }

  Widget _buildSection(String title, String content) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: AppDimensions.paddingSmall),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: const TextStyle(
                fontSize: 16,
                fontWeight: FontWeight.bold,
                color: AppThemes.primaryNavy),
          ),
          const SizedBox(height: AppDimensions.paddingSmallX),
          Text(
            content,
            style: TextStyle(fontSize: 14, color: Colors.grey.shade700, height: 1.5),
          ),
          const SizedBox(height: AppDimensions.paddingSmall),
          const Divider(),
        ],
      ),
    );
  }
